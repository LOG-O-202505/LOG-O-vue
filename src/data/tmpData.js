// 데이터 가져오기 함수들 - 백엔드 API 대신 사용
export const getTravelDestinations = (regionCode = null, sigCode = null, limit = 100) => {
  let filteredDestinations = [];
  
  if (sigCode) {
    // 시군구 코드로 필터링 (가장 작은 단위)
    filteredDestinations = allDestinations.filter(dest => dest.sig === sigCode);
  } else if (regionCode) {
    // 광역시도 코드로 필터링
    filteredDestinations = allDestinations.filter(dest => dest.region === regionCode);
  } else {
    // 전국 데이터 (기본값)
    filteredDestinations = [...allDestinations];
  }
  
  // 중복 제거 로직 개선 - 이름과 위치(location)를 기준으로 중복 제거
  const uniqueDestinationsMap = new Map();
  
  filteredDestinations.forEach(dest => {
    // 이름과 위치를 조합한 고유 키 생성
    const key = `${dest.name}_${dest.location}`;
    // 이미 동일한 이름과 위치 조합이 있으면 평점이 더 높은 것으로 유지
    if (!uniqueDestinationsMap.has(key) || uniqueDestinationsMap.get(key).rating < dest.rating) {
      uniqueDestinationsMap.set(key, dest);
    }
  });
  
  // 중복 제거된 목록 생성
  const uniqueDestinations = Array.from(uniqueDestinationsMap.values());
  
  // 평점 및 원본 순위 기준 정렬 (수정된 정렬 로직)
  uniqueDestinations.sort((a, b) => {
    if (b.rating !== a.rating) {
      return b.rating - a.rating; // 1. 평점 내림차순
    }
    if (a.rank !== b.rank) {
      return a.rank - b.rank;     // 2. 원본 순위 오름차순 (낮을수록 좋음)
    }
    return a.id - b.id;             // 3. ID 오름차순 (안정적인 정렬)
  });
  
  // 실제 순위 적용 (수정된 displayRank 로직: 단순 순차적 순위)
  uniqueDestinations.forEach((dest, index) => {
    dest.displayRank = index + 1; // 1부터 시작하는 순차적 순위
  });
  
  // 요청한 개수만큼 반환
  return uniqueDestinations.slice(0, limit);
};

// 지역별 인기도 점수 계산 함수
export const calculateRegionPopularityScore = () => {
  // TOP 100 여행지 가져오기
  const top100Destinations = getTravelDestinations(null, null, 100);
  
  // 광역시도별 점수 합계를 저장할 객체
  const regionScores = {};
  
  // 각 여행지에 순위에 따른 점수 부여 (1등: 100점, 100등: 1점)
  top100Destinations.forEach((dest, index) => {
    const score = 100 - index; // 100점부터 시작해서 1점까지
    const regionCode = dest.region;
    
    // 지역 코드별로 점수 합산
    if (!regionScores[regionCode]) {
      regionScores[regionCode] = 0;
    }
    regionScores[regionCode] += score;
  });
  
  // 점수를 배열 형태로 변환하고 점수 기준 내림차순 정렬
  const regionScoreArray = Object.entries(regionScores).map(([regionCode, score]) => ({
    regionCode,
    score
  })).sort((a, b) => b.score - a.score);
  
  // 최대 점수와 최소 점수 찾기
  const maxScore = regionScoreArray.length > 0 ? regionScoreArray[0].score : 0;
  const minScore = regionScoreArray.length > 0 ? regionScoreArray[regionScoreArray.length - 1].score : 0;
  
  // 점수 범위
  const scoreRange = maxScore - minScore;
  
  // 정규화된 점수 계산 (0~1 범위로)
  const normalizedScores = {};
  regionScoreArray.forEach(item => {
    // 점수가 모두 같으면 1로 설정, 아니면 정규화
    normalizedScores[item.regionCode] = scoreRange === 0 
      ? 1 
      : (item.score - minScore) / scoreRange;
  });
  
  return {
    regionScores: normalizedScores, // 0~1 사이로 정규화된 점수
    rawScores: regionScoreArray     // 원본 점수 및 순위 정보
  };
};

